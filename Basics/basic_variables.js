// Difference between let, const and var
// let and const are block-scoped, while var is function-scoped
// let and const are not hoisted, while var is hoisted
// const is immutable, while let and var are mutable
// const must be initialized during declaration, while let and var can be initialized later
// const cannot be re-declared, while let and var can be re-declared
// const is recommended for variables that should not be re-assigned
// let is recommended for variables that should be re-assigned
// var is not recommended in ES6

